from torch import nn
import torch
import numpy as np

class AddSpatiotemporalPositionEmbs(nn.Module):
    def __init__(self,grid_size,embed_dim) -> None:
        super().__init__()
        self.temporal_size=grid_size[0]
        self.space_size=grid_size[1:]
        self.pos_embed_spatial = nn.Parameter(torch.zeros(1, grid_size[1] * grid_size[2], embed_dim))
        self.pos_embed_temporal = nn.Parameter(torch.zeros(1, grid_size[0], embed_dim))
        self.initialize_weights()
        
    def initialize_weights(self):
        torch.nn.init.normal_(self.pos_embed_spatial, std=.02)
        torch.nn.init.normal_(self.pos_embed_temporal, std=.02)
        setattr(self.pos_embed_spatial,'no_weight_decay',True)
        setattr(self.pos_embed_temporal,'no_weight_decay',True)
        pass
    def forward(self,x):
        # add pos embed w/o cls token
        pos_embed = self.pos_embed_spatial.repeat(1, self.temporal_size, 1) + \
                    torch.repeat_interleave(self.pos_embed_temporal, self.space_size[0] * self.space_size[1], dim=1)
        return x+ pos_embed

class Add1DLearnablePositionEmbs(nn.Module):
    def __init__(self,grid_size,embed_dim) -> None:
        super().__init__()
        n_patches=grid_size[0]*grid_size[1]*grid_size[2]
        self.pos_embed=nn.Parameter(torch.zeros(1,n_patches,embed_dim))

    def initialize_weights(self):
        torch.nn.init.normal_(self.pos_embed, std=.02)
        setattr(self.pos_embed,'no_weight_decay',True)
        pass

    def forward(self,x):
        return x + self.pos_embed

class AddSinusoidalPositionEmbs(nn.Module):

    def __init__(self, embed_dim, n_position=200):
        super(AddSinusoidalPositionEmbs, self).__init__()

        # Not a parameter
        self.register_buffer('pos_table', self._get_sinusoid_encoding_table(n_position, embed_dim))

    def _get_sinusoid_encoding_table(self, n_position, embed_dim):
        ''' Sinusoid position encoding table '''
        # TODO: make it with torch instead of numpy

        def get_position_angle_vec(position):
            return [position / np.power(10000, 2 * (hid_j // 2) / embed_dim) for hid_j in range(embed_dim)]

        sinusoid_table = np.array([get_position_angle_vec(pos_i) for pos_i in range(n_position)])
        sinusoid_table[:, 0::2] = np.sin(sinusoid_table[:, 0::2])  # dim 2i
        sinusoid_table[:, 1::2] = np.cos(sinusoid_table[:, 1::2])  # dim 2i+1

        return torch.FloatTensor(sinusoid_table).unsqueeze(0)

    def forward(self, x):
        return x + self.pos_table[:, :x.size(1)].clone().detach() # type: ignore

